create policy "Anyone can add matchday player ratings"
on public.matchday_player_ratings
for insert
to anon
with check (true);

drop policy if exists "Anyone can update matchday player ratings" on public.matchday_player_ratings;
create policy "Anyone can update matchday player ratings"
on public.matchday_player_ratings
for update
to anon
using (true)
with check (true);

