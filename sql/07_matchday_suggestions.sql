create or replace function public.enforce_matchday_suggestion_like_player_on_roster()
returns trigger
language plpgsql
as $$
declare
  target_matchday_id bigint;
  roster_exists boolean;
begin
  if new.suggestion_id is not null then
    select suggestion.matchday_id
    into target_matchday_id
    from public.matchday_suggestions suggestion
    where suggestion.id = new.suggestion_id;
  else
    select suggestion.matchday_id
    into target_matchday_id
    from public.matchday_suggestion_replies reply
    join public.matchday_suggestions suggestion
      on suggestion.id = reply.suggestion_id
    where reply.id = new.suggestion_reply_id;
  end if;

  if target_matchday_id is null then
    raise exception using
      errcode = '23503',
      message = 'A valid suggestion target is required before saving matchday_suggestion_likes.';
  end if;

  select exists (
    select 1
    from public.matchdays md
    join public.season_players sp
      on sp.season_id = md.season_id
    where md.id = target_matchday_id
      and sp.player_id = new.liked_by_player_id
  )
  into roster_exists;

  if not roster_exists then
    raise exception using
      errcode = '23514',
      message = format(
        'Player %s must belong to the season roster before saving matchday_suggestion_likes.',
        new.liked_by_player_id
      ),
      detail = format(
        'suggestion_id=%s, suggestion_reply_id=%s, liked_by_player_id=%s',
        new.suggestion_id,
        new.suggestion_reply_id,
        new.liked_by_player_id
      ),
      hint = 'Add the player to season_players for the related matchday season, then retry.';
  end if;

  return new;
end;
$$;

create table if not exists public.matchday_suggestion_likes (
  id bigint generated by default as identity primary key,
  suggestion_id bigint references public.matchday_suggestions(id) on delete cascade,
  suggestion_reply_id bigint references public.matchday_suggestion_replies(id) on delete cascade,
  liked_by_player_id bigint not null references public.players(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint matchday_suggestion_likes_target_check check (
    (suggestion_id is not null and suggestion_reply_id is null)
    or (suggestion_id is null and suggestion_reply_id is not null)
  )
);

