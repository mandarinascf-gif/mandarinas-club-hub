create or replace function public.enforce_matchday_suggestion_reply_author_on_roster()
returns trigger
language plpgsql
as $$
declare
  target_matchday_id bigint;
  roster_exists boolean;
begin
  select suggestion.matchday_id
  into target_matchday_id
  from public.matchday_suggestions suggestion
  where suggestion.id = new.suggestion_id;

  if target_matchday_id is null then
    raise exception using
      errcode = '23503',
      message = format(
        'Suggestion %s must exist before saving matchday_suggestion_replies.',
        new.suggestion_id
      ),
      detail = format('suggestion_id=%s', new.suggestion_id);
  end if;

  select exists (
    select 1
    from public.matchdays md
    join public.season_players sp
      on sp.season_id = md.season_id
    where md.id = target_matchday_id
      and sp.player_id = new.author_player_id
  )
  into roster_exists;

  if not roster_exists then
    raise exception using
      errcode = '23514',
      message = format(
        'Player %s must belong to the season roster for suggestion %s before saving matchday_suggestion_replies.',
        new.author_player_id,
        new.suggestion_id
      ),
      detail = format('suggestion_id=%s, author_player_id=%s', new.suggestion_id, new.author_player_id),
      hint = 'Add the player to season_players for the related matchday season, then retry.';
  end if;

  return new;
end;
$$;

