-- Incremental upgrade: matchday suggestion threads with replies and likes.
-- Run this after sql/06_player_submission_foundation.sql on existing projects.

begin;

create or replace function public.enforce_matchday_suggestion_author_on_roster()
returns trigger
language plpgsql
as $$
declare
  roster_exists boolean;
begin
  select exists (
    select 1
    from public.matchdays md
    join public.season_players sp
      on sp.season_id = md.season_id
    where md.id = new.matchday_id
      and sp.player_id = new.author_player_id
  )
  into roster_exists;

  if not roster_exists then
    raise exception using
      errcode = '23514',
      message = format(
        'Player %s must belong to the season roster for matchday %s before saving matchday_suggestions.',
        new.author_player_id,
        new.matchday_id
      ),
      detail = format('matchday_id=%s, author_player_id=%s', new.matchday_id, new.author_player_id),
      hint = 'Add the player to season_players for the matchday season, then retry.';
  end if;

  return new;
end;
$$;

create table if not exists public.matchday_suggestions (
  id bigint generated by default as identity primary key,
  matchday_id bigint not null references public.matchdays(id) on delete cascade,
  author_player_id bigint not null references public.players(id) on delete cascade,
  title text not null,
  body text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint matchday_suggestions_title_length check (
    char_length(trim(title)) between 3 and 120
  ),
  constraint matchday_suggestions_body_length check (
    char_length(trim(body)) between 1 and 1200
  )
);

create index if not exists matchday_suggestions_matchday_idx
on public.matchday_suggestions (matchday_id, created_at desc);

create index if not exists matchday_suggestions_author_idx
on public.matchday_suggestions (author_player_id, created_at desc);

drop trigger if exists matchday_suggestions_set_updated_at on public.matchday_suggestions;

create trigger matchday_suggestions_set_updated_at
before update on public.matchday_suggestions
for each row
execute procedure public.set_updated_at();

drop trigger if exists matchday_suggestions_require_season_roster on public.matchday_suggestions;

create constraint trigger matchday_suggestions_require_season_roster
after insert or update of matchday_id, author_player_id on public.matchday_suggestions
deferrable initially deferred
for each row
execute procedure public.enforce_matchday_suggestion_author_on_roster();

alter table public.matchday_suggestions enable row level security;

